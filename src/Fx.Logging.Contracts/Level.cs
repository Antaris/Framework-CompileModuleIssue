namespace Fx.Logging
{
    /// <summary>
    /// Defines the logging levels.
    /// </summary>
    public enum Level
    {
        /// <summary>
        /// Debug messages.
        /// </summary>
        Debug,

        /// <summary>
        /// Information messages.
        /// </summary>
        Info,

        /// <summary>
        /// Warning messages.
        /// </summary>
        Warning,

        /// <summary>
        /// Non-fatal error messages.
        /// </summary>
        Error,

        /// <summary>
        /// Fatal error messages.
        /// </summary>
        Fatal
    }
}